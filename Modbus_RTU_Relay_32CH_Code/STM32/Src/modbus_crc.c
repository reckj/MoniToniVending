const uint16_t CRCTableAbs[] = { 
  0x0000, 0xCC01, 0xD801, 0x1400, 0xF001, 0x3C00, 0x2800, 0xE401, 
  0xA001, 0x6C00, 0x7800, 0xB401, 0x5000, 0x9C01, 0x8801, 0x4400 
};

uint16_t ModbusCRC_CheckTable(uint8_t *ptr, uint16_t len)
{
  uint8_t crcHigh = 0xff;
  uint8_t crcLow = 0xff; 
  uint16_t index;
  while (len--) 
  {
    index = crcLow ^ *ptr++; 
    crcLow = crcHigh ^ CRCTableHigh[index];
    crcHigh = CRCTableLow[index];
  }
  return (crcHigh << 8 | crcLow);
}

uint16_t ModbusCRC_CheckTableAbs(uint8_t *ptr, uint16_t len) 
{
  uint16_t crc = 0xffff; 
  uint16_t i;
  uint8_t ch;
  
  for (i = 0; i < len; i++) 
  {
    ch = *ptr++;
    crc = CRCTableAbs[(ch ^ crc) & 15] ^ (crc >> 4);
    crc = CRCTableAbs[((ch >> 4) ^ crc) & 15] ^ (crc >> 4);
  } 
  
  return crc;
}

uint16_t ModbusCRC_Calc(uint8_t *ptr, uint16_t len)
{
  uint8_t i;
  uint16_t crc = 0xffff;
  
  if (len == 0) {
    len = 1;
  }
  while (len--) {
    crc ^= *ptr;
    for (i = 0; i<8; i++)
    {
      if (crc & 1) {
        crc >>= 1;
        crc ^= polynom16;
      }
      else {
        crc >>= 1;
      }
    }
    ptr++;
  }
  return(crc);
}



uint32_t STM32CRC_IntCalc(uint32_t *ptr, int len)
{
  uint32_t xbit, data;
  uint32_t crc = 0xffffffff;    // init value
  
  while (len--) {
    xbit = 0x80000000;
    
    data = *ptr++;
    for (int bits = 0; bits < 32; bits++) 
    {
      if (crc & 0x80000000) {
        crc <<= 1;
        crc ^= polynom32;
      }
      else
        crc <<= 1;
      
      if (data & xbit)
        crc ^= polynom32;
      
      xbit >>= 1;
    }
  }
  return crc;
}

uint32_t STM32CRC_CharCalc(uint8_t *p, uint32_t len) 
{  
  // fast bit by bit algorithm without augmented zero bytes.
  // does not use lookup table.
  
  uint32_t i, j, c, bit;
  uint32_t crchighbit = 0x80000000;
  uint32_t crc = 0xffffffff;
  
  for (i = 0; i < len; i++)
  {
    c = (uint32_t)*p++;
    
    for (j = 0x80; j; j >>= 1) {
      
      bit = crc & crchighbit;
      crc <<= 1;
      
      if (c & j) 
        bit ^= crchighbit;
      
      if (bit) 
        crc ^= polynom32;
    }
  }
  
  return(crc);
}
